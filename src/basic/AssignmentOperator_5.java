package basic;

public class AssignmentOperator_5 {

	public static void main(String[] args) {
		// Assignment operator : are used to assign values to variable 
		// they are : =, +=, -=, *=, /=, %=
		
		int x= 5; // x=5
		System.out.println("Actual x:"+ x);
		   x += 3;
		System.out.println("x:"+ x); // 8
		
		// -=
		int z=10;
		System.out.println("z:"+z);
		z -= 5;
		System.out.println("new value of z :"+z);
		
		//
		int m =3;
		    m *= 2;
		System.out.println("m:"+m);
		
		// /=
		int d =20;
		    d/=5;
		System.out.println("d:"+d);
		
		// %=
		int apple_box = 10;
		apple_box %= 3;
		System.out.println("Apple box:"+apple_box); //1
		
		
		
		
		

	}

}
