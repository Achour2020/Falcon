package basic;

public class DoWhileAlphabet {

	public static void main(String[] args) {
		/*
		 
		 */
		// increment
		char c = 'a';
		do {
			System.out.print(" "+c);
			c++;
		} while (c <= 'z');
		
		System.out.println("\n");
		
		// decrement 
		
		char ch = 'z';
		do {
			System.out.print(" "+ ch);
			ch--;
		} while (ch >= 'a');
		
		
		
		

	}

}
