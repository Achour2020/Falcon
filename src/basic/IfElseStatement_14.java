package basic;

public class IfElseStatement_14 {

	public static void main(String[] args) {
		// if else: keywords --> decision making to control our selection statements
				// relation operators: >, <, ==, <=, >=, != 

				int number = 10;

				// relational operators
				if (number != 10) { // true block
					System.out.println("True Statement / true code block");
				} else { // false
					System.out.println("False Statement / false code block");
				}
				
				// another example
				System.out.println("\n Another Example of if else statment");
				
				int age = 16; 
				if (age >= 18) {
					System.out.println("Eligible for a DMV lincense"); // true
				} else {
					System.out.println("NOT eligible for a DMV license");
				}
				
				{ // code block // scope of the block 
					
				}


	}

}
