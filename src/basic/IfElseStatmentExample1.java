package basic;

public class IfElseStatmentExample1 {

	public static void main(String[] args) {
		// if else
		
		int number = 20;
		
		// Condition
		if (number >= 10) { // true block
			System.out.println("True: the number is greater/equal to 10:" + number);
		} else { // false
			System.out.println("False: the number is not greater/equal to 10:" + number);
		}

		// 
	}

}
